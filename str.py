print("lowercase:", name.lower())
print("capitalize:", name.capitalize())
print("Title case:", name.title())
print("stripped:", name.strip())

print("is alphabetic:", name.isalpha())
print("is alphanumeric:", name.isalnum())
print("Replace e with @:", name.replace('e','@'))
print("is digit:", name.isdigit())
print("count:", name.count('o'))
print("is endswith 123:", name.endswith('123'))
